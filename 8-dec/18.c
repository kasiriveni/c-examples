/* switch statement with break*/
#include<stdio.h>
int main()
{
  	int i=2;
  	switch(i)
  	{
    	case 0:
		        printf("\n Inside case0");
		        break;
    	case 1:
		        printf("\n Inside case1");
		        break;
	    case 2:
		        printf("\n Inside case2");
		        break;
	    case 3:
		        printf("\n Inside case3");
		        break;
    	default:
		        printf("\n Inside default case");
  	}
	return 0;
}
