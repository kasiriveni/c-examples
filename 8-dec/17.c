/* a  program on switch statement without break*/

#include<stdio.h>
int main()
{
  	int i=2;
  	switch(i)
  	{
    	case 0:
		        printf("\n Inside case0");

    	case 1:
		        printf("\n Inside case1");

	    case 2:
		        printf("\n Inside case2");

	    case 3:
		        printf("\n Inside case3");

    	default:
		        printf("\n Inside default case");
  	}
	return 0;
}
